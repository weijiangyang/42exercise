#include <stdio.h>

int	ft_recursive_factorial(int nb);

int	main(void)
{
	int	result;

	result = ft_recursive_factorial(6);
	printf ("%d", result);
	return (0);
}

int	ft_recursive_factorial(int nb)
{
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	return (nb * ft_recursive_factorial(nb - 1));
}
