#include <stdio.h>
#include <string.h>

int	ft_strlen(char *str);

int	main(void)
{
	int	result;

	result = ft_strlen("hello, weijiang");
	printf ("length of this str is %d", result);
	return (0);
}

int	ft_strlen(char *str)
{
	int	count;

	count = 0;
	while (str[count] != '\0')
	{
		count ++;
	}
	return (count);
}
